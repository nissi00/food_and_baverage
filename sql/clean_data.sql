-- ============================================================
-- Phase 1 – Étape 5 : Data Cleaning (BRONZE → SILVER)
-- ============================================================

USE DATABASE ANYCOMPANY_LAB;
CREATE SCHEMA IF NOT EXISTS SILVER;

-- ============================================================
-- 1. FINANCIAL_TRANSACTIONS_CLEAN
-- ============================================================
CREATE OR REPLACE TABLE ANYCOMPANY_LAB.SILVER.FINANCIAL_TRANSACTIONS_CLEAN AS
SELECT
    TRANSACTION_ID,
    TRANSACTION_DATE,
    TRANSACTION_TYPE,
    AMOUNT,
    PAYMENT_METHOD,
    TRIM(ENTITY) AS ENTITY,
    TRIM(REGION) AS REGION,
    ACCOUNT_CODE
FROM ANYCOMPANY_LAB.BRONZE.FINANCIAL_TRANSACTIONS
WHERE TRANSACTION_ID IS NOT NULL
  AND TRANSACTION_DATE IS NOT NULL
  AND AMOUNT > 0
QUALIFY ROW_NUMBER() OVER (PARTITION BY TRANSACTION_ID ORDER BY TRANSACTION_DATE DESC) = 1;

-- ============================================================
-- 2. PROMOTIONS_CLEAN
-- ============================================================
CREATE OR REPLACE TABLE ANYCOMPANY_LAB.SILVER.PROMOTIONS_CLEAN AS
SELECT
    PROMOTION_ID,
    TRIM(PRODUCT_CATEGORY) AS PRODUCT_CATEGORY,
    TRIM(PROMOTION_TYPE) AS PROMOTION_TYPE,
    DISCOUNT_PERCENTAGE,
    START_DATE,
    END_DATE,
    TRIM(REGION) AS REGION
FROM ANYCOMPANY_LAB.BRONZE.PROMOTIONS_DATA
WHERE PROMOTION_ID IS NOT NULL
  AND START_DATE IS NOT NULL
  AND END_DATE IS NOT NULL
  AND START_DATE <= END_DATE
  AND DISCOUNT_PERCENTAGE > 0
QUALIFY ROW_NUMBER() OVER (PARTITION BY PROMOTION_ID ORDER BY START_DATE DESC) = 1;

