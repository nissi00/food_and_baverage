-- ============================================================
-- 6. PRODUCT_REVIEWS_CLEAN
-- ============================================================
CREATE OR REPLACE TABLE ANYCOMPANY_LAB.SILVER.PRODUCT_REVIEWS_CLEAN AS
SELECT
    REVIEW_ID,
    PRODUCT_ID,
    REVIEWER_ID,
    TRIM(REVIEWER_NAME) AS REVIEWER_NAME,
    HELPFUL_VOTES,
    TOTAL_VOTES,
    RATING,
    REVIEW_DATE::DATE AS REVIEW_DATE,
    TRIM(REVIEW_TITLE) AS REVIEW_TITLE,
