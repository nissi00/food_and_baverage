  AND REVIEW_DATE IS NOT NULL
QUALIFY ROW_NUMBER() OVER (PARTITION BY REVIEW_ID ORDER BY REVIEW_DATE DESC) = 1;

-- ============================================================
-- 7. INVENTORY_CLEAN (JSON → table relationnelle)
-- ============================================================
CREATE OR REPLACE TABLE ANYCOMPANY_LAB.SILVER.INVENTORY_CLEAN AS
SELECT
    f.VALUE:"product_id"::VARCHAR AS PRODUCT_ID,
    TRIM(f.VALUE:"product_category"::VARCHAR) AS PRODUCT_CATEGORY,
    TRIM(f.VALUE:"region"::VARCHAR) AS REGION,
    TRIM(f.VALUE:"country"::VARCHAR) AS COUNTRY,
    TRIM(f.VALUE:"warehouse"::VARCHAR) AS WAREHOUSE,
    f.VALUE:"current_stock"::NUMBER AS CURRENT_STOCK,
    f.VALUE:"reorder_point"::NUMBER AS REORDER_POINT,
    f.VALUE:"lead_time"::NUMBER AS LEAD_TIME,
    f.VALUE:"last_restock_date"::DATE AS LAST_RESTOCK_DATE
FROM ANYCOMPANY_LAB.BRONZE.INVENTORY,
     LATERAL FLATTEN(INPUT => RAW_JSON) f
WHERE f.VALUE:"product_id" IS NOT NULL
  AND f.VALUE:"current_stock"::NUMBER >= 0;

